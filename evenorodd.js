// 1. Create a function to check if a given number is even or odd and display information to user.

function EvenOrOdd(num){
    if(num%2 ==0){
        console.log(num + " is an even number");
    }
    else{
        console.log( num +" is an odd number")
    }
}
EvenOrOdd(2);
EvenOrOdd(1);
EvenOrOdd(0);
EvenOrOdd(100);
